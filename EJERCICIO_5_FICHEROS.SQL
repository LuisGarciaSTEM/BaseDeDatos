DROP SCHEMA IF EXISTS EJERCICIO_5;
CREATE SCHEMA EJERCICIO_5;

USE EJERCICIO_5;

# PUBLICACIÓN (ISSN, TÍTULO, NÚMERO, AÑO)
CREATE TABLE PUBLICACION(
	ISSN CHAR (8) PRIMARY KEY,
    TITULO VARCHAR (50),
    NUMERO SMALLINT,
	ANIO SMALLINT
);

# ARTÍCULO (TÍTULO, PAGINA_INICIO, PAGINA_FIN, ISSN)
CREATE TABLE ARTICULO(
	TITULO VARCHAR (50) PRIMARY KEY,
    PAGINA_INICIO TINYINT,
    PAGINA_FIN SMALLINT,
    ISSN_PUBLICACION CHAR(8),
    FOREIGN KEY (ISSN_PUBLICACION) REFERENCES PUBLICACION(ISSN)
);

# AUTOR (ID, NOMBRE, DEPARTAMENTO, E-MAIL)
CREATE TABLE AUTOR(
	ID INT PRIMARY KEY AUTO_INCREMENT,
    NOMBRE VARCHAR (50),
    E_MAIL VARCHAR(50),
    DEPARTAMENTO VARCHAR(50)
);

# AUTOR_ESCRIBE_ARTICULO (ID_AUTOR, TÍTULO_ARTICULO, POSICION)
CREATE TABLE AUTOR_ESCRIBE_ARTICULO(
	ID_AUTOR INT,
    TITULO_ARTICULO VARCHAR(50),
    POSICION TINYINT,
    
    PRIMARY KEY (ID_AUTOR, TITULO_ARTICULO),
    
    FOREIGN KEY (ID_AUTOR) REFERENCES AUTOR(ID),
    FOREIGN KEY (TITULO_ARTICULO) REFERENCES ARTICULO(TITULO)   
);

