DROP SCHEMA IF EXISTS PRUEBA_TECNICA_2;
CREATE SCHEMA PRUEBA_TECNICA_2;
USE PRUEBA_TECNICA_2;

CREATE TABLE CRIATURA (
	ID INT PRIMARY KEY AUTO_INCREMENT,
    NOMBRE VARCHAR (50),
    EDAD SMALLINT
    
);

CREATE TABLE HOBBIT (
	ID_CRIATURA INT PRIMARY KEY,
    NOMBRE_FAMILIA VARCHAR (50),
    PIE_NO_CALZA SMALLINT, 
    DISTANCIA_COMARCA FLOAT,
    
    FOREIGN KEY (ID_CRIATURA) REFERENCES CRIATURA (ID)
    
);
